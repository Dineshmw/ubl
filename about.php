        <h3>History</h3>
        <p>The University of Peradeniya has recognized Career Development as an integral part of the academic training of graduates for wisdom since 2001 by appointing a Part-time Director, for Career Guidance to address the career development needs of students. From 2001 to 2004, only the director served in the unit and conducted career-related activities mainly in collaboration with the Peradeniya University Alumni Association. At the begining, a single office room located at the WUS student center was used for both the Career Guidance Unit and the Alumni Association.</p>
        <p>Since 2005 the Career Guidance Unit (CGU) of the University of Peradeniya was functional with two trained Career Guidance Counsellors. In the year 2005, the first full-time Director was appointed and he served for 10 years continuously. During the last 20 years, the CGU offered the services of both psychological counselling and career advising for students to guide them for academic and career success. In the year 2016, the third Director was appointed on a part-time basis for three years and since 2017, the main focus of the CGU was aligned on career development in line with the UGC guidelines and directives.</p>
        <p>In the year 2017, a Career Guidance Advisory Board was established with the chairmanship of the Vice-Chancellor for guiding the activities of the unit. In parallel to that, a Faculty coordinators forum was also formed in the same year to link the activities of the CGU with faculties. In the year 2018, a students’ ambassador network was also established by appointing one ambassador for each Department to liaise directly with students.</p>
        <p>In the year 2017, “Robarosiya” students’ society was established and hosted by the CGU to train student leaders for the personality development of peer students and provide community services to the society. In the year 2018, the CGU initiated a Gavel Club under its roof to enhance the leadership and communication skills of undergraduates. In the year 2021, a certificate course on career development was offered for the first time in the history.</p>
